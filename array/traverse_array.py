def traverse_array(arr):
    for element in arr:
        print(element)
    
# Example usage
arr=[10,20,30,40,50]
traverse_array(arr)
# Output: 10 20 30 40 50

#Alogorithm:
# when question asks to traverse an array, it means to visit each element in the array and perform some operation (like printing, summing, etc.).
# In this case, we are simply printing each element of the array.       
#mainly used for loop for this type of operation.
